package org.pcsoft.app.jimix.commons.exception;

public class JimixPluginAnnotationException extends JimixPluginException {
    public JimixPluginAnnotationException() {
    }

    public JimixPluginAnnotationException(String message) {
        super(message);
    }

    public JimixPluginAnnotationException(String message, Throwable cause) {
        super(message, cause);
    }

    public JimixPluginAnnotationException(Throwable cause) {
        super(cause);
    }
}
